package com.stylefeng.guns.rest.utilBeans;

public class StatusBean {
    private int status;
    private String msg;

    public StatusBean() {
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
